## This function caches the inverse of a matrix using the built in 
## solve command. It first searches for the cached value of the
## inverse and returns that if found

## Creates an object that caches it's own inverse. Set the matrix as
## makeCacheMatrix(matrix(...))

makeCacheMatrix <- function(x = numeric()) {
  m <- NULL
  set <- function(y) {
    x <<- y
    m <<- NULL
  }
  get <- function() x
  setsolve <- function(solve) m <<- solve
  getsolve <- function() m
  list(set = set, get = get,
       setsolve = setsolve,
       getsolve = getsolve)
}

## Then use this command to caculate the inverse of the object
## created using the previous command

cacheSolve <- function(x, ...) {
        ## Return a matrix that is the inverse of 'x'
  m <- x$getsolve()
  if(!is.null(m)) {
    message("getting cached data")
    return(m)
  }
  data <- x$get()
  m <- solve(data, ...)
  x$setsolve(m)
  m
  
}
